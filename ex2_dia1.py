numero_a = input("Digite o primeiro número: ")
numero_b = input("Digite o segundo número: ")
numero_a_float = float(numero_a)
numero_b_float = float(numero_b)

soma = numero_a_float + numero_b_float
sub = numero_a_float - numero_b_float
multi = numero_a_float * numero_b_float
div = numero_a_float / numero_b_float

print(f"Número 1: {numero_a_float}")
print(f"Número 2: {numero_b_float}")
print(f"Soma: {soma}")
print(f"Subtração: {sub}")
print(f"Multiplicação: {multi}")
print(f"Divisão: {div}")
